window.tmpl = function (key, vars = {}) {
  const lang = localStorage.getItem("lang") || "en";

  let text = DICT[lang]?.[key] || DICT["en"]?.[key] || key;

  Object.keys(vars).forEach(k => {
    text = text.replace(new RegExp(`{${k}}`, "g"), vars[k]);
  });

  return text;
};

